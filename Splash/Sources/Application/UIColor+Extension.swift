import UIKit

extension UIColor {
    open class var paleGreen: UIColor {
        return UIColor(hexString: "#98fb98")
    }
    open class var oceanBlue: UIColor {
        return UIColor(hexString: "#2887c2")
    }
    open class var paleGreenDark: UIColor {
        return UIColor(hexString: "#79c879")
    }
    open class var oceanBlueDark: UIColor {
        return UIColor(hexString: "#0E2F44")
    }
}
